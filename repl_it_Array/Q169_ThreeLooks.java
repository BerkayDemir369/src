package repl_it_Array;

public class Q169_ThreeLooks {

	  /*This one is about truth table logic, 
	   * threeLocks gets 3 booleans and returns a boolean.

       It returns true only if both a and b are true or c is true*/
	    
	   public boolean threeLocks(boolean a, boolean b, boolean c) {
		    
	       return (a && b) || c;
	    
	  }//end threeLocks
}
